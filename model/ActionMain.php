  public $reference;
  public $name;
  public $idActionType;
  public $idProject;
  public $creationDate;
  public $idUser;
  public $idPriority;
  public $idContact;
  public $isPrivate;
  public $description;
  public $_sec_treatment;
  public $idStatus;
  public $idResource;
  public $initialDueDate;
  public $actualDueDate;
  public $handled;
  public $handledDate;
  public $done;
  public $doneDate;
  public $idle;
  public $idleDate;
  public $cancelled;
  public $_lib_cancelled;
  public $idEfficiency;
  public $result;
  
  //public $_sec_linkRisk;
